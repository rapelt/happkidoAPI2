package happkidoAPI.gradingHistory;

public class GradingHistory {

    private String date;
    private String grade;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }
}
